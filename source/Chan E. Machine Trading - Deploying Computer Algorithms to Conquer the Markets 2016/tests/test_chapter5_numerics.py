def test_black_scholes_independent_invariants(results):
    theory = results["option_theory"]
    assert abs(theory["parity_error"]) < 1e-10
    assert abs(theory["delta_finite_difference_error"]) < 1e-7
    assert abs(theory["gamma_finite_difference_error"]) < 1e-6
    assert theory["recovered_sigma"] == pytest.approx(
        theory["inputs"]["sigma"], abs=1e-10
    )
    assert np.all(np.isfinite(theory["call_leverage"]))
    assert np.all(np.isfinite(theory["put_leverage"]))


