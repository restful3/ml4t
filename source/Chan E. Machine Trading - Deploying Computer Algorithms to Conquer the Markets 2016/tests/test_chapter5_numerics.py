def test_generated_notebook_is_fully_executed(chapter5):
    if not chapter5.NOTEBOOK_PATH.exists():
        pytest.skip("notebook is generated by the chapter entrypoint")
    notebook = nbformat.read(chapter5.NOTEBOOK_PATH, as_version=4)
    code = [cell for cell in notebook.cells if cell.cell_type == "code"]
    assert code and all(cell.execution_count is not None for cell in code)
    assert not any(
        output.output_type == "error" for cell in code for output in cell.outputs
    )
    png_count = sum(
        "image/png" in output.get("data", {})
        for cell in code
        for output in cell.outputs
    )
    assert png_count == 5


